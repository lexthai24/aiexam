const L = ["ก", "ข", "ค", "ง"];

// Helper: build a question from four choice texts + correct label.
function q(
  number: number,
  category: string,
  text: string,
  choiceTexts: [string, string, string, string],
  correct: string
): SeedQuestion {
  return {
    key: `q${number}`,
    number,
    category,
    text,
    choices: choiceTexts.map((t, i) => ({ label: L[i], text: t })),
    correct,
  };
}

